    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        try {

            DefaultTableModel dtm = (DefaultTableModel) tabelaCliente.getModel();
            
            if (dtm.getRowCount() > 0) {
                for (int i = 0; i < dtm.getRowCount(); i++) {
                    dtm.removeRow(0);
                }
            }
            
            if (chkCpf.isSelected() && Validador.validaString(txtCpfPesquisa)) {
                txtCpf.setText(txtCpfPesquisa.getText());
                
                this.cliente.setCpf(txtCpfPesquisa.getText());
                
                txtCpfPesquisa.setText(null);

                txtNome.setEnabled(true);
                txtCpf.setEnabled(true);
                txtRg.setEnabled(true);
                dtDataNasc.setEnabled(true);
                cboSexo.setEnabled(true);
                txtTelefone.setEnabled(true);
                txtEmail.setEnabled(true);
                
                try {
                    this.clientes = ClienteController.consultarBy(this.cliente, chkCpf.isSelected());
                    for (Cliente c : this.clientes) {
                        dtm.addRow(new Object[]{
                            c.getId(),
                            c.getNome(),
                            c.getCpf(),
                            c.getTelefone(),
                            c.getCidade()
                        });
                    }
                    
                } catch (IllegalArgumentException e) {
                    Logger.getLogger(FrmCadastroCliente.class.getName()).log(Level.SEVERE, null, e);
                    JOptionPane.showMessageDialog(this, e.getMessage(), "Cliente não encontrado", JOptionPane.INFORMATION_MESSAGE);
                } catch (SQLException ex) {
                    Logger.getLogger(FrmCadastroCliente.class.getName()).log(Level.SEVERE, null, ex);
                    JOptionPane.showMessageDialog(this, ex.getMessage(), "Cliente não encontrado, causa: " + ex.getMessage(), JOptionPane.INFORMATION_MESSAGE);
                }

            }

            if (!chkCpf.isSelected() && Validador.validaString(txtNomePesquisa)) {
                txtNome.setText(txtNomePesquisa.getText());
                this.cliente.setNome(txtNomePesquisa.getText());
                
                 txtNomePesquisa.setText(null);

                txtNome.setEnabled(true);
                txtCpf.setEnabled(true);
                txtRg.setEnabled(true);
                dtDataNasc.setEnabled(true);
                cboSexo.setEnabled(true);
                txtTelefone.setEnabled(true);
                txtEmail.setEnabled(true);

                try {
                    this.clientes = ClienteController.consultarBy(this.cliente, chkCpf.isSelected());
                    for (Cliente c : this.clientes) {
                        dtm.addRow(new Object[]{
                            c.getId(),
                            c.getNome(),
                            c.getCpf(),
                            c.getTelefone(),
                            c.getCidade()
                        });
                    }
                } catch (SQLException e) {
                    Logger.getLogger(FrmCadastroCliente.class.getName()).log(Level.SEVERE, null, e);
                    JOptionPane.showMessageDialog(this, "Cliente não encontrado", "Cliente não encontrado", JOptionPane.INFORMATION_MESSAGE);
                }
            }

        } catch (IllegalArgumentException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Campo obrigatório", 3);
        }
    }//GEN-LAST:event_btnConsultarActionPerformed

    private void btnPesquisaCepActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisaCepActionPerformed
        try {
            if (Validador.validaString(txtCepCliente)) {
                Cep cep = ClienteController.consultarCep(txtCepCliente.getText());

                txtBairro.setEnabled(true);
                txtLogradouro.setEnabled(true);
                txtNumeroCasaCliente.setEnabled(true);
                cboxEstado.setEnabled(true);
                txtCidadeCliente.setEnabled(true);
                txtComplementoCliente.setEnabled(true);

                if (cep.getLogradouro() != null) {
                    txtBairro.setText(cep.getBairro());
                    txtCidadeCliente.setText(cep.getLocalidade());
                    txtLogradouro.setText(cep.getLogradouro());

                    int indexUf = -1;
                    for (int i = 0; i < cboxEstado.getItemCount(); i++) {
                        if (cboxEstado.getItemAt(i).equals(cep.getUf())) {
                            indexUf = i;
                        }
                    }

                    cboxEstado.setSelectedIndex(indexUf);
                } else {
                    JOptionPane.showMessageDialog(this, "CEP não encontrado, favor informar um correto!", "Cep não encontrado", JOptionPane.ERROR_MESSAGE);
                }

            }
        } catch (IOException ex) {
            Logger.getLogger(FrmCadastroCliente.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IllegalArgumentException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Campo obrigatório", 3);
        }
    }//GEN-LAST:event_btnPesquisaCepActionPerformed

